package co.yedam.puppy.booking.vo;

public class BookingVO {
	
	
	private int no;
	private String company_owner;
	private String company_name;
	private String saupja_no;
	private String company_tel;
	private String company_zip;
	private String company_addr;
	private int bank_use;
	private String bank_account;
	private int partner_no;
	private int category_no;
	private int location_no;
	
	public BookingVO() {} 
	
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getCompany_owner() {
		return company_owner;
	}
	public void setCompany_owner(String company_owner) {
		this.company_owner = company_owner;
	}
	public String getCompany_name() {
		return company_name;
	}
	public void setCompany_name(String company_name) {
		this.company_name = company_name;
	}
	public String getSaupja_no() {
		return saupja_no;
	}
	public void setSaupja_no(String saupja_no) {
		this.saupja_no = saupja_no;
	}
	public String getCompany_tel() {
		return company_tel;
	}
	public void setCompany_tel(String company_tel) {
		this.company_tel = company_tel;
	}
	public String getCompany_zip() {
		return company_zip;
	}
	public void setCompany_zip(String company_zip) {
		this.company_zip = company_zip;
	}
	public String getCompany_addr() {
		return company_addr;
	}
	public void setCompany_addr(String company_addr) {
		this.company_addr = company_addr;
	}
	public int getBank_use() {
		return bank_use;
	}
	public void setBank_use(int bank_use) {
		this.bank_use = bank_use;
	}
	public String getBank_account() {
		return bank_account;
	}
	public void setBank_account(String bank_account) {
		this.bank_account = bank_account;
	}
	public int getPartner_no() {
		return partner_no;
	}
	public void setPartner_no(int partner_no) {
		this.partner_no = partner_no;
	}
	public int getCategory_no() {
		return category_no;
	}
	public void setCategory_no(int category_no) {
		this.category_no = category_no;
	}
	public int getLocation_no() {
		return location_no;
	}
	public void setLocation_no(int location_no) {
		this.location_no = location_no;
	}
	
	
	

}
